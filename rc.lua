-- Create widget that will be on the wibar and may be shared by different
-- screens
local mylauncher = require("mywidgets.launcher").create()
local mytextclock = wibox.widget.textclock()
local mybattery = require("mywidgets.battery").create()

-- Import modules which need to be called on a per screen basis.
local mylayoutbox = require("mywidgets.layoutbox")
local mytaglist = require("mywidgets.taglist")
local mytasklist = require("mywidgets.tasklist")

-- connect to each attached screen and create a basic desktop using the
-- imported widgets
awful.screen.connect_for_each_screen(function(s)
	-- Wallpaper
	set_wallpaper(s)

	-- Each screen has its own tag table.
	awful.tag({ "1", "2", "3", "4", "5", "6", "7", "8", "9" }, s, awful.layout.layouts[1])

	-- Create an imagebox widget which will contain an icon indicating which layout we're using.
	-- We need one layoutbox per screen.
	s.mylayoutbox = mylayoutbox.create(s)

	-- Create a taglist widget
	s.mytaglist = mytaglist.create(s)

	-- Create a tasklist widget
	s.mytasklist = mytasklist.create(s)

	-- Create the wibox
	s.mywibox = awful.wibar({ position = "top", screen = s })

	-- Add widgets to the wibox
	s.mywibox:setup({
		layout = wibox.layout.align.horizontal,
		{ -- Left widgets
			layout = wibox.layout.fixed.horizontal,
			mylauncher,
			s.mytaglist,
		},
		s.mytasklist, -- Middle widget
		{ -- Right widgets
			layout = wibox.layout.fixed.horizontal,
			spacing = 10,
			wibox.widget.systray(),
			mybattery,
			mytextclock,
			s.mylayoutbox,
		},
	})
end)
-- }}}

-- {{{ Key bindings
-- Default modkey.
-- Usually, Mod4 is the key with a logo between Control and Alt.
-- If you do not like this or do not have such a key,
-- I suggest you to remap Mod4 to another key using xmodmap or other tools.
-- However, you can use another modifier like Mod1, but it may interact with others.
modkey = "Mod4"
globalkeys = require("mykeys.global").create(modkey)
clientkeys = require("mykeys.client").create(modkey)

clientbuttons = gears.table.join(
	awful.button({}, 1, function(c)
		c:emit_signal("request::activate", "mouse_click", { raise = true })
	end),
	awful.button({ modkey }, 1, function(c)
		c:emit_signal("request::activate", "mouse_click", { raise = true })
		awful.mouse.client.move(c)
	end),
	awful.button({ modkey }, 3, function(c)
		c:emit_signal("request::activate", "mouse_click", { raise = true })
		awful.mouse.client.resize(c)
	end)
)

